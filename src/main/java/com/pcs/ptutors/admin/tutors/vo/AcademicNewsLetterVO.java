package com.pcs.ptutors.admin.tutors.vo;

/**
 *
 * @author Dipayan
 */
public class AcademicNewsLetterVO implements java.io.Serializable {
    private String newstitle;
    private String newsdate;
    private String newsdescription;
    private String newswebsite;

    /**
     * @return the newstitle
     */
    public String getNewstitle() {
        return newstitle;
    }

    /**
     * @param newstitle the newstitle to set
     */
    public void setNewstitle(String newstitle) {
        this.newstitle = newstitle;
    }

    /**
     * @return the newsdate
     */
    public String getNewsdate() {
        return newsdate;
    }

    /**
     * @param newsdate the newsdate to set
     */
    public void setNewsdate(String newsdate) {
        this.newsdate = newsdate;
    }

    /**
     * @return the newsdescription
     */
    public String getNewsdescription() {
        return newsdescription;
    }

    /**
     * @param newsdescription the newsdescription to set
     */
    public void setNewsdescription(String newsdescription) {
        this.newsdescription = newsdescription;
    }

    /**
     * @return the newswebsite
     */
    public String getNewswebsite() {
        return newswebsite;
    }

    /**
     * @param newswebsite the newswebsite to set
     */
    public void setNewswebsite(String newswebsite) {
        this.newswebsite = newswebsite;
    }

    


}
