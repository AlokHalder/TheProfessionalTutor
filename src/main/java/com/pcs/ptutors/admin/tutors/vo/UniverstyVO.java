public class UniverstyVO {

    private String countrycode;
    private String locationid;
    private String universityinstitutename;
    private String universityid;

    

    /**
     * @return the countrycode
     */
    public String getCountrycode() {
        return countrycode;
    }

    /**
     * @param countrycode the countrycode to set
     */
    public void setCountrycode(String countrycode) {
        this.countrycode = countrycode;
    }

    /**
     * @return the locationid
     */
    public String getLocationid() {
        return locationid;
    }

    /**
     * @param locationid the locationid to set
     */
    public void setLocationid(String locationid) {
        this.locationid = locationid;
    }

    /**
     * @return the universityinstitutename
     */
    public String getUniversityinstitutename() {
        return universityinstitutename;
    }

    /**
     * @param universityinstitutename the universityinstitutename to set
     */
    public void setUniversityinstitutename(String universityinstitutename) {
        this.universityinstitutename = universityinstitutename;
    }

    /**
     * @return the universityid
     */
    public String getUniversityid() {
        return universityid;
    }

    /**
     * @param universityid the universityid to set
     */
    public void setUniversityid(String universityid) {
        this.universityid = universityid;
    }


    




}
